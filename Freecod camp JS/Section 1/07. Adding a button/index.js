// Create a variable, bonusPoints. Initialize it as 50. Increase it to 100.
// Decrease it down to 25, and then finally increase it to 70
// Console.log the value after each step

let bonusPoints = 50; // bonusPoints adında bir değişken oluşturulur ve başlangıç değeri 50 olarak atanır
console.log(bonusPoints); // bonusPoints değeri konsola yazdırılır (50)

bonusPoints = bonusPoints + 50; // bonusPoints değerine 50 eklenir
console.log(bonusPoints); // bonusPoints değeri konsola yazdırılır (100)

bonusPoints = bonusPoints - 75; // bonusPoints değerinden 75 çıkarılır
console.log(bonusPoints); // bonusPoints değeri konsola yazdırılır (25)

bonusPoints = bonusPoints + 45; // bonusPoints değerine 45 eklenir
console.log(bonusPoints); // bonusPoints değeri konsola yazdırılır (70)



